        public static void SetLanguage(string lang) => _lang = lang;
        public static string CurrentLanguage => _lang;

        public static string T(string key, Dictionary<string, object>? vars = null)
        {
            var dict = _lang switch { "zh-CN" => ZhCN, "en" => En, _ => ZhTW };
            var raw = dict.TryGetValue(key, out var v) ? v : key;
            if (vars == null) return raw;
            return Regex.Replace(raw, @"\{\{(\w+)\}\}", m =>
                vars.TryGetValue(m.Groups[1].Value, out var val) ? val.ToString()! : m.Value);
        }

        public static string[] Days()
        {
            return _lang switch
            {
                "en"    => new[] { "Sun","Mon","Tue","Wed","Thu","Fri","Sat" },
                "zh-CN" => new[] { "日","一","二","三","四","五","六" },
                _       => new[] { "日","一","二","三","四","五","六" },
            };
        }

